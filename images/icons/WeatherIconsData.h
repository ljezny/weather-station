// Weather code to icon mapping function
inline const uint8_t* getWeatherIcon(int weatherCode, bool isDay = true) {
    switch (weatherCode) {
        case 0:  // Clear sky
            return isDay ? BITMAP_SUN : BITMAP_MOON;
        case 1:  // Mainly clear
            return isDay ? BITMAP_SUN : BITMAP_MOON;
        case 2:  // Partly cloudy
            return BITMAP_PARTLY_CLOUDY;
        case 3:  // Overcast
            return BITMAP_CLOUD;
        case 45: // Fog
        case 48: // Depositing rime fog
            return BITMAP_FOG;
        case 51: // Light drizzle
        case 53: // Moderate drizzle
        case 55: // Dense drizzle
        case 56: // Light freezing drizzle
        case 57: // Dense freezing drizzle
            return BITMAP_DRIZZLE;
        case 61: // Slight rain
        case 63: // Moderate rain
        case 65: // Heavy rain
        case 66: // Light freezing rain
        case 67: // Heavy freezing rain
        case 80: // Slight rain showers
        case 81: // Moderate rain showers
        case 82: // Violent rain showers
            return BITMAP_RAIN;
        case 71: // Slight snow
        case 73: // Moderate snow
        case 75: // Heavy snow
        case 77: // Snow grains
        case 85: // Slight snow showers
        case 86: // Heavy snow showers
            return BITMAP_SNOW;
        case 95: // Thunderstorm
        case 96: // Thunderstorm with slight hail
        case 99: // Thunderstorm with heavy hail
            return BITMAP_THUNDER;
        default:
            return BITMAP_CLOUD;
    }
}
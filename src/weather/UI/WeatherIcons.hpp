// Helper function to get weather icon based on WMO weather code
inline const uint8_t* getWeatherIcon(int weatherCode, bool isDay = true) {
    // WMO Weather interpretation codes
    // https://open-meteo.com/en/docs
    
    if (weatherCode == 0) {
        // Clear sky
        return isDay ? BITMAP_SUN : BITMAP_MOON;
    } else if (weatherCode == 1 || weatherCode == 2) {
        // Partly cloudy
        return BITMAP_PARTLY_CLOUDY;
    } else if (weatherCode == 3) {
        // Overcast
        return BITMAP_CLOUD;
    } else if (weatherCode >= 45 && weatherCode <= 48) {
        // Fog
        return BITMAP_FOG;
    } else if (weatherCode >= 51 && weatherCode <= 57) {
        // Drizzle
        return BITMAP_DRIZZLE;
    } else if (weatherCode >= 61 && weatherCode <= 67) {
        // Rain
        return BITMAP_RAIN;
    } else if (weatherCode >= 71 && weatherCode <= 77) {
        // Snow
        return BITMAP_SNOW;
    } else if (weatherCode >= 80 && weatherCode <= 82) {
        // Rain showers
        return BITMAP_RAIN;
    } else if (weatherCode >= 85 && weatherCode <= 86) {
        // Snow showers
        return BITMAP_SNOW;
    } else if (weatherCode >= 95 && weatherCode <= 99) {
        // Thunderstorm
        return BITMAP_THUNDER;
    }
    
    // Default to cloud
    return BITMAP_CLOUD;
}
